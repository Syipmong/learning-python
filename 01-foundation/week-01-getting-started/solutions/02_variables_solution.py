# Solution 2: Variables and Data Types

# 1. Create a variable for your name
name = "Alice"

# 2. Create a variable for your age
age = 25

# 3. Create a variable for your height in meters
height = 1.75

# 4. Create a boolean variable indicating if you like Python
likes_python = True

# 5. Print all variables with descriptive messages
print("My name is:", name)
print("My age is:", age)
print("My height is:", height, "meters")
print("I like Python:", likes_python)

# Alternative using f-strings (more advanced)
print(f"Hello! My name is {name}, I'm {age} years old and {height}m tall.")
print(f"Do I like Python? {likes_python}!")
